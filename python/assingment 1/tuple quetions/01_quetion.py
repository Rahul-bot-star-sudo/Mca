# Given tuple
t = (10, 20, 30, 40, 50)

# i) Display the first three elements
first_three = t[:3]
print("(i) First three elements:", first_three)

# ii) Find the maximum and minimum element
maximum = max(t)
minimum = min(t)
print("(ii) Maximum element:", maximum)
print("     Minimum element:", minimum)

# iii) Check whether 30 exists in the tuple
exists = 30 in t
print("(iii) Does 30 exist in the tuple?:", exists)
