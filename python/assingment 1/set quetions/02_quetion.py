# Given sets
A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

# i) Union of A and B
union_set = A | B  # or A.union(B)
print("(i) Union of A and B:", union_set)

# ii) Intersection of A and B
intersection_set = A & B  # or A.intersection(B)
print("(ii) Intersection of A and B:", intersection_set)

# iii) Difference of A and B (A - B)
difference_set = A - B  # or A.difference(B)
print("(iii) Difference of A and B (A - B):", difference_set)
