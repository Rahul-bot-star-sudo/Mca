# Program: Create a list of first letters from each word using list comprehension

# Original list
names = ['Ajay', 'Vijay', 'Ganesh', 'Paresh', 'Mahesh']

# List comprehension to get the first letter of each name
first_letters = [name[0] for name in names]

# Display result
print("Input List:", names)
print("Output List:", first_letters)
